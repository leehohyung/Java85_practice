// 주제 : 메모리를 사용하는 방법 - 문자값 저장

public class Exam023 {
  public static void main(String[] args) {
    char v1 = 0x41;
    char v2 = 0xAC00;
    char v3 = 0x31;
    char v4 = '공';

    System.out.printf("v1=%c\n", v1);
    System.out.printf("v2=%c\n", v2);
    System.out.printf("v3=%c\n", v3);
    System.out.printf("v4=%c\n", v4);
  }
}

