//주제: 표현 가능한 값의 범위 - 논리값

public class Exam015 {
  public static void main(String[] args) {
    System.out.println(true);
    System.out.println(false);

    System.out.println(10 < 20); // true
    System.out.println(10 > 20); // false
    System.out.println(10 == 20); // false
    System.out.println(10 != 20); // true
  }
}

