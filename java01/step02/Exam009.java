//주제: 값을 표현하는 방법(literal)

public class Exam009 {
  public static void main(String[] args) {
    System.out.println(85); // 정수 값
    System.out.println(3.14); // 부동소수점 값
    System.out.println(true); // 논리 값
    System.out.println(false); // 논리 값
    System.out.println('A'); // 문자
    System.out.println("A"); // 문자열. 한 자 이상의 문자들.
    System.out.println("문자열"); // 문자열. 한 자 이상의 문자들.
    //System.out.print('AB'); // 오류!
  }
}

