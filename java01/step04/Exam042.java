//주제: 연산자 - 관계 연산자 사용법

public class Exam042 {
  public static void main(String[] args) {
    // 조건을 따질 때 많이 사용한다.
    System.out.printf("10 == 20 ? %b\n", 10 == 20);
    System.out.printf("10 != 20 ? %b\n", 10 != 20);
    System.out.printf("10 < 20 ? %b\n", 10 < 20);
    System.out.printf("10 <= 20 ? %b\n", 10 <= 20);
    System.out.printf("10 > 20 ? %b\n", 10 > 20);
    System.out.printf("10 >= 20 ? %b\n", 10 >= 20);
  }
}

